    Nosso objetivo foi criar um algoritmo que mostre o IMC do individuo, juntamente com os seus percentuais corporais para maior compreensão dos seus dados corporais!
    Atualmente vivemos na Geração Saúde, onde grande parte da população busca alternativas saudáveis como treinos funcionais, academia e exercícios ao ar livre como caminhada ou corridas. 
    Com isso surge um grande duvida: “ Como estão os meus Níveis Corporais? “.  Com essa duvida, a principal solução encontrada por grande parte das pessoas é  fazer uma avaliação corporal com algum profissional da área.
    Porém existe um problema, essas avaliações feitas por profissionais, as vezes se tornam inviáveis financeiramente, com valores que giram em torno de R$80 a R$150 dependendo do profissional.
    Com base nessa problemática,  criamos um método para que as pessoas consigam saber seus níveis corporais sem a necessidade de pagar um profissional.

